  // 기본 타입 : 산수, 수학에서 사용하는 데이터이자 값 자체. 정수, 실수, 문자, 불린
  // 참조 타입 : 여러 기본 타입을 묶어서 사용. 참조 타입은 new 키워드를 이용하여 객체를 생성해서 사용
  // String은 참조 타입이지만 기본 타입처럼 사용하는 특이한 녀석

