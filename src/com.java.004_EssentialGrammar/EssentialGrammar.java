  // 비교 연산
  // 모든 기본 타입의 값을 비교할 때는 비교 연산을 사용한다.
  // 비교 연산은 boolean을 반환 한다.
  // 6>5 //true 크다
  // 6<5 //false 작다
  // 6>=5 //true 크거나 같다
  // 6<=5 //false 작거나 같다
  // 6==5 //false 같다
