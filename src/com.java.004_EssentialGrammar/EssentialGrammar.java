  float f1 = 10.234F; // 소수점이 없는 경우 최대 크기는 총 39 자릿수로 표시
  float f2 = 10.234f; // 소수점만 있는 경우 최소 크기는 소수점 아래에 45자릿수까지 표시 가능
  double d = 10.234; // 소수점이 없는 경우 최대 크기는 총 309자릿수로 표시 , 소수점만 있는경우 소수점 아래로 총 325자릿수 표시

