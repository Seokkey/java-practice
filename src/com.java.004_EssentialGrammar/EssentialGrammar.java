  // 작은 타입은 큰 타입에 자동 형 변환 가능. 묵시적 형 변환
  // 큰 타입은 작은 타입에 대입하려면 캐스팅을 해줘야한다. 명시적 형 변환, 큰 컵에 있는 물을 작은 컵에 담을 경우를 상상
