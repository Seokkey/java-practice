  // latitudeA, latitudeB 는 변수 37.5212722505154, 38.52121 는 데이터
  // 변수는 컵, 데이터는 컵에 담을 물.
  // 물의 양과 종류에 따라 담을 컵이 달라야 한다.
  double latitudeA = 37.5212722505154;

  double latitudeB = 38.52121;

  // 기본 타입의 크기
  // 정수                   // 저장 범위
  byte b = 100; // -128 ~ 127
  short s = 100; // -32,768~32,767
  int i = 100; // -2,147,483,648 ~ 2,147,483,647
  long l1 = 100L; // -9,223,372,036,854,775,80, ~ 9,223,372,036,854,775,807
  long l2 = 100l; // long은 값 뒤에 L 이나 l 을 붙여줘야 한다 대문자 권장

