  // 실수와 정수는 기본적으로 5칙연산을 사용한다. +,-,*,/(나눗셈-몫),%(나눗셈- 나머지, 모듈러스)
  // 정수끼리 연산하면 결과도 정수
  // 실수와 정수를 연산하면 결과는 실수
  // 5+5=10
  // 5-5=0
  // 5*5=25
  // 5/6=0
